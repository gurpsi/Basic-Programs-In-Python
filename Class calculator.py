class calculator:
    def addition(a,b):
        add=a+b
        print(add)

    def subtraction(a,b):
        sub=a-b
        print(sub)

    def multiplication(a,b):
        multi=a*b
        print(multi)

    def division(a,b):
        div=a/b
        print(div)

calculator.multiplication(9,10)
calculator.addition(2,8)
calculator.subtraction(10,20)
calculator.division(10,5)